CREATE
  TABLE MNL_ORDER
  (
    order_id      NUMBER(19,0),
    order_type_id VARCHAR2(60 CHAR),
    sr_no         VARCHAR2(30 CHAR) ,
    bkg_no        VARCHAR2(20 CHAR),
    frm_route     NUMBER(19,0),
    to_route      NUMBER(19,0),
    trans_mode    VARCHAR2(30 CHAR),
    CONTRACT_ID   NUMBER(19,0),
    SUPPLIER_ID   NUMBER(19,0 ),
  CONSTRAINT MNL_ORDER_pk PRIMARY KEY (order_id),
  CONSTRAINT MNL_ORDER_fk1 FOREIGN KEY (sr_no) REFERENCES MNL_SHP_LEG(sr_num),
  CONSTRAINT MNL_ORDER_fk2 FOREIGN KEY (CONTRACT_ID) REFERENCES MNL_CONTRACT(CONTRACT_ID),
  CONSTRAINT MNL_ORDER_fk3 FOREIGN KEY (SUPPLIER_ID) REFERENCES MNL_SUPPLIER(SUPPLIER_ID)
  );



CREATE
  TABLE MNL_CONTRACT
  (
    CONTRACT_ID NUMBER(19,0),
    AMOUNT      NUMBER(30,10),
    CONSTRAINT MNL_CONTRACT PRIMARY KEY (CONTRACT_ID)
  ); 

CREATE
  TABLE MNL_SUPPLIER
  (
    SUPPLIER_ID NUMBER(19,0),
    CODE        VARCHAR2(255 CHAR),
    NAME        VARCHAR2(4000 CHAR),
    CONSTRAINT MNL_SUPPLIER PRIMARY KEY (SUPPLIER_ID)
  ); 
  
  CREATE 
  TABLE MNL_CHG_DETAILS
  (
	CHARGE_ID number(19, 0),
	ORDER_ID NUMBER(19,0),
	CONTRACT_ID NUMBER(19,0),
	TTL_AMOUNT NUMBER(19,0),
		CONSTRAINT MNL_CHG_DETAILS_PK PRIMARY KEY (CHARGE_ID),
	CONSTRAINT MNL_CHG_DETAILS_fk1 FOREIGN KEY (CONTRACT_ID) REFERENCES MNL_CONTRACT(CONTRACT_ID),
	CONSTRAINT MNL_CHG_DETAILS_fk2 FOREIGN KEY (ORDER_ID) REFERENCES MNL_ORDER(order_id)
	
  );
  
