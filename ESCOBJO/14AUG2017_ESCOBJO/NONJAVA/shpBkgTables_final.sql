ALTER TABLE MNL_SHP_CGO ADD FOREIGN KEY (cms_ref_num) REFERENCES MNL_SHP_CMS(cms_ref_num); 
ALTER TABLE MNL_SHP_RTE ADD FOREIGN KEY (nsso_rte_id) REFERENCES MNL_SSO_ROUTE(route_id);
