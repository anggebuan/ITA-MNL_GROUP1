CREATE TABLE MNL_SHP_SHIPMENT (
	shipment_num varchar2(20 char) NOT NULL PRIMARY KEY,
	status varchar2(20 char),
	create_iodt varchar2(20 char),
	update_iodt varchar2(20 char)
);

CREATE TABLE MNL_SHP_CMS (
    cms_ref_num varchar2(10 char) NOT NULL PRIMARY KEY,
    shipment_num varchar2(20 char),
    cntr_num varchar2(10 char),
    size_type varchar2(4 char),
    rte_id varchar2(20 char),
    wt_magnitude number(10,0),
    create_iodt varchar2(20 char),
    update_iodt varchar2(20 char)    
);

CREATE TABLE MNL_SHP_CGO (
	cgo_id varchar2(10 char) NOT NULL PRIMARY KEY,
  	cms_ref_num varchar2(10 char),
	cgo_desc varchar2(100 char),
	create_iodt varchar2(20 char),
	update_iodt varchar2(20 char)	
);

CREATE TABLE MNL_SHP_RTE (
	rte_id varchar2(20 char) NOT NULL PRIMARY KEY,
	nsso_rte_id varchar2(20 char),
	create_iodt varchar2(20 char),
	update_iodt varchar2(20 char)
);

CREATE TABLE MNL_SHP_LEG (
	leg_id varchar2(20 char) NOT NULL PRIMARY KEY,
	rte_id varchar2(20 char),
	sr_num varchar2(30 char),
	from_event_id varchar2(20 char),
	to_event_id varchar2(20 char),
	create_iodt varchar2(20 char),
	update_iodt varchar2(20 char)
);

CREATE TABLE MNL_SHP_EVENT (
	event_id varchar2(20 char) NOT NULL PRIMARY KEY,
	eqp_id number(19,0),
	create_iodt varchar2(20 char),
	update_iodt varchar2(20 char)
);

CREATE TABLE MNL_SHP_SR (
	sr_num varchar2(30 char) NOT NULL PRIMARY KEY,
	order_id number(19,0),
	create_iodt varchar2(20 char),
	update_iodt varchar2(20 char)
);

