ALTER TABLE MNL_SHP_LEG ADD FOREIGN KEY (sr_num) REFERENCES MNL_SHP_SR(sr_num); 
ALTER TABLE MNL_SHP_LEG ADD FOREIGN KEY (from_event_id) REFERENCES MNL_SHP_EVENT(event_id); 
ALTER TABLE MNL_SHP_LEG ADD FOREIGN KEY (to_event_id) REFERENCES MNL_SHP_EVENT(event_id);
